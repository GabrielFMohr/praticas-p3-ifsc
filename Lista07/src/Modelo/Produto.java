package Modelo;

public class Produto {

	private String nome;
	private Long CodBarra;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Long getCodBarra() {
		return CodBarra;
	}

	public void setCodBarra(Long codBarra) {
		CodBarra = codBarra;
	}
}
